// tostring 在逆向过程中是非常常见的

// 数组转字符串
let param = [5, 6, 8];
console.log(param.toString());

// 字符串转二进制
let value = 6;
console.log(value.toString(2));

// 字符串转16进制
let value2 = 26;
console.log(value2.toString(16));

// 布尔值转字符串
let value1 = false;
console.log(value1.toString());

// 判断类型
let arr = [1,2,3];
console.log(toString.call(arr));